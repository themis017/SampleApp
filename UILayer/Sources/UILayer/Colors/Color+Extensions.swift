import Foundation
import SwiftUI

public extension Color {
    
    // MARK: Primary Button
    
    static let primaryButtonEnabledColor = Color(uiColor: UIColor.primaryButtonEnabledColor)
    static let primaryButtonDisabledColor = Color(uiColor: UIColor.primaryButtonDisabledColor)
    
    // MARK: Secondary Button
    
    static let secondaryButtonEnabledColor = Color(uiColor: UIColor.secondaryButtonEnabledColor)
    static let secondaryButtonDisabledColor = Color(uiColor: UIColor.secondaryButtonDisabledColor)
    
    // MARK: Tab bar
    
    static let tabBarBackgroundColor = Color(uiColor: UIColor.tabBarBackgroundColor)
    static let tabBarCategoryPrimaryColor = Color(uiColor: UIColor.tabBarCategoryPrimaryColor)
    static let tabBarCategorySecondaryColor = Color(uiColor: UIColor.tabBarCategorySecondaryColor)
}
